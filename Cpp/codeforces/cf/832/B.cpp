using ll = long long;

void solve() {
    int n;
    cin >> n;
    if (n == 1) {
        cout << 1 << endl;
        cout << 1 << " " << 2 << endl;
        return;
    }
    cout << (n + 1) / 2 << endl;
    int i = 2, j = 3 * (n / 2 + 1);
    while (j <= 3 * n) {
        cout << i << " " << j << endl;
        i += 3;
        j += 3;
    }
}

int main(int argc, char const *argv[])
{
    ios::sync_with_stdio(false);
    cin.tie(0);
    int t;
    cin >> t;
    while (t--) {
        solve();
    }
    return 0;
}
