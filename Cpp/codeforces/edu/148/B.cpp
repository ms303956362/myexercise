using namespace std;

using ll = long long;

void solve() {
    ll n, k;
    cin >> n >> k;
    vector<ll> a(n);
    for (int i = 0; i < n; ++i) {
        cin >> a[i];
    }
    sort(a.begin(), a.end());
    vector<ll> pre(n + 1);
    for (int i = n - 1; i >= 0; --i) {
        pre[i] = pre[i + 1] + a[i];
    }
    int i = 2 * k, j = n;
    ll ans = 0;
    while (i >= 0) {
        ans = max(ans, pre[i] - pre[j]);
        i -= 2;
        --j;
    }
    cout << ans << '\n';
}

int main(int argc, char const *argv[])
{
    ios::sync_with_stdio(false);
    cin.tie(0);
    int t;
    cin >> t;
    while (t--) {
        solve();
    }
    return 0;
}
