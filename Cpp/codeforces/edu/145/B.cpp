using namespace std;

using ll = long long;

void solve() {
    ll n;
    cin >> n;
    ll l = 0, h = 1e9 + 5, ans = 0;
    while (l <= h) {
        ll m = (l + h) / 2;
        if ((m + 1) * (m + 1) >= n) {
            ans = m;
            h = m - 1;
        } else {
            l = m + 1;
        }
    }
    cout << ans << '\n';
}

int main(int argc, char const *argv[])
{
    ios::sync_with_stdio(false);
    cin.tie(0);
    int t;
    cin >> t;
    while (t--) {
        solve();
    }
    return 0;
}
