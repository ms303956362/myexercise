using namespace std;

using ll = long long;

void solve() {
    int n;
    cin >> n;
    int cnt1 = 0, cnt2 = 0;
    for (int i = 0; i < n; ++i) {
        int h;
        cin >> h;
        if (h == 1) {
            ++cnt1;
        } else {
            ++cnt2;
        }
    }
    cout << cnt2 + cnt1 % 2 + cnt1 / 2 << '\n';
}

int main(int argc, char const *argv[])
{
    ios::sync_with_stdio(false);
    cin.tie(0);
    int t;
    cin >> t;
    while (t--) {
        solve();
    }
    return 0;
}
