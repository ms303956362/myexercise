using namespace std;

using ll = long long;

int main(int argc, char const *argv[])
{
    ios::sync_with_stdio(false);
    cin.tie(0);
    int n;
    cin >> n;
    vector<int> a(n);
    for (int i = 0; i < n; ++i) {
        cin >> a[i];
    }
    sort(a.begin(), a.end());
    int ans = 0;
    for (int i = 0; i < n - 1; ++i) {
        if (a[i] + 1 != a[i + 1]) {
            ans = a[i] + 1;
            break;
        }
    }
    cout << ans << '\n';
    return 0;
}
